function Configurator() {
  const [controller, dispatch] = useMaterialUIController();
  const {
    openConfigurator,
    fixedNavbar,
    sidenavColor,
    transparentSidenav,
    whiteSidenav,
    darkMode,
  } = controller;

  // Disable the configurator overlay
  const handleCloseConfigurator = () => setOpenConfigurator(dispatch, false);

  useEffect(() => {
    // Automatically close the configurator on component mount
    handleCloseConfigurator();
  }, []);

  // Prevent rendering the Configurator UI
  return null;
}

